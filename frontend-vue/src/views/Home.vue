const observeSections = () => {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          const sectionName = entry.target.dataset.section
          if (sectionName === 'stats') {
            statsVisible.value = true
            animateCounters()
          } else if (sectionName) {
            sectionsVisible.value[sectionName] = true
          }
        }
      })
    },
    { threshold: 0.2 }
  )

  document.querySelectorAll('[data-section]').forEach((section) => {
    observer.observe(section)
  })

  return observer
}

const animateCounters = () => {
  const duration = 2000
  const steps = 60
  const stepDuration = duration / steps

  const animate = (target, key, suffix = '') => {
    let current = 0
    const increment = target / steps
    const timer = setInterval(() => {
      current += increment
      if (current >= target) {
        animatedStats.value[key] = target + suffix
        clearInterval(timer)
      } else {
        animatedStats.value[key] = Math.floor(current) + suffix
      }
    }, stepDuration)
  }

  animate(250, 'biens', '+')
  animate(15, 'annees', '+')
  animate(98, 'satisfaction', '%')
  animate(50, 'projets', '+')
}

onMounted(async () => {
  try {
    services.value = await api.getServices()
    
    const propsData = await api.getProperties()
    if (Array.isArray(propsData)) {
      properties.value = propsData.slice(0, 6).map(p => ({
        ...p,
        mainImage: api.getImageUrl(p.mainImage)
      }))
    } else {
      console.error('Properties data is not an array:', propsData)
      properties.value = []
    }

    const projRows = await api.getProjects()
      projects.value = projRows.map(r => ({
        id: r.id,
        slug: r.slug,
        title: r.title,
        category: r.category || r.status || 'Projet',
        location: r.location || '',
        image: api.getImageUrl(
          r.coverImage || (r.medias?.find(m => m.kind === 'image')?.url) || '/placeholder.svg'
        ),
        year: r.startedAt ? String(new Date(r.startedAt).getFullYear()) : ''
      }))
  } catch (error) {
    console.error('Error loading data:', error)
  }

  setTimeout(() => {
    observeSections()
  }, 100)
})
</script>

